#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int nbr;

	nbr = ft_abs(n);
	if (n < 0)
		ft_putchar_fd('-', fd);
	if (nbr > 9)
	{
		ft_putnbr_fd(nbr / 10, fd);
		ft_putnbr_fd(nbr % 10, fd);
	}
	else
		ft_putchar_fd(nbr + 48, fd);
}
